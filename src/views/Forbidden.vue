<template>
  <div class="flex justify-center h-screen mt-40">
    <div class="px-40 py-2">
      <div class="flex flex-col items-center">
        <h1 class="font-bold text-blue-600 text-9xl">403</h1>

        <h6 class="mb-2 text-2xl font-bold text-center text-gray-800 md:text-3xl">
          <span class="text-red-500">Oops!</span>
        </h6>

        <p class="mb-8 text-center text-gray-500 md:text-lg">Forbidden: Access is denied.</p>

        <a href="#" class="px-6 py-2 text-sm font-semibold text-blue-800 bg-blue-100" @click="goHome">Go home</a>
      </div>
    </div>
  </div>
</template>

<script>
import { useRouter } from 'vue-router'
import { useStore } from 'vuex'
import { computed } from 'vue'

export default {
  name: 'Forbidden',
  setup() {
    const store = useStore()
    const router = useRouter()

    const isAuth = computed(() => {
      return store.state.auth.isAuth
    })

    const goHome = () => {
      if (isAuth.value) {
        router.push({ path: '/' })
      } else {
        router.push({ name: 'login' })
      }
    }

    return {
      goHome,
    }
  },
}
</script>
